int		ft_atoi(const char *c)
{
	int value;
	int sign;
	int i;

	value = 0;
	sign = 1;
	i = 0;
	while (c[i] == ' ' || c[i] == '\n' || c[i] == '\t' ||
			c[i] == '\f' || c[i] == '\r' || c[i] == '\v' || c[i] == '+')
		i++;
	if (c[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (c[i] >= 48 && c[i] <= 57)
	{
		value *= 10;
		value += ((int)c[i] - '0');
		c++;
	}
	return (value * sign);
}
