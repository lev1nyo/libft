#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char *s;

	s = dst;
	while (len > 0 && *src != '\0')
	{
		*s++ = *src++;
		--len;
	}
	while (len > 0)
	{
		*s++ = '\0';
		--len;
	}
	return (dst);
}
